<?php

function langFormat($lang) {
	if ($lang == "fr_FR")
		return "🇫🇷 Français";
	if ($lang == "en_UK")
		return "🇬🇧 English";
	if ($lang == "es_ES")
		return "🇪🇸 Español";
	if ($lang == "it_IT")
		return "🇮🇹 Italiano";
	if ($lang == "de_DE")
		return "🇩🇪 Deutsch";
	if ($lang == "nl_NL")
		return "🇳🇱 Netherlands";
	if ($lang == "ja_JP")
		return "🇯🇵 Japan";
	if ($lang == "zh_TW")
		return "🇨🇳 Chinese";
}
